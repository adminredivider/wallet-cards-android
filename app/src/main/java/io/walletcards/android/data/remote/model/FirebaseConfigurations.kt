package io.walletcards.android.data.remote.model

data class FirebaseConfigurations(
    val buttonWithTrial: String,
    val buttonWithoutTrial: String,
    val buttonWithIntroductoryPrice: String,
    val cancelButton: String,
    val continueLimitedVersion: String,
    val socialProofBottomLine: String,
    val socialProofHeadline: String,
    val socialProofSubline: String,
    val removeAdsHeadline: String,
    val removeAdsContent: String,
    val cancelTimer: Int,
    val confirmExit: String,
    val tutorialButton: String,
    val tutorialUrl: String,
    val paywallBulletPoints: List<String>,
    var removeAdsButton: String,
    var recoveryNotificationInterval: Int,
    val enforcedAppVersion: String,
    var forceUpdateTitle: String,
    var forceUpdateSubtitle: String,
    var forceUpdateDescription: String,
    var buttonText: String,
    var dismissText: String,
    var enforcedAppVersionLastAccepted: String,
    var ratingsHeadline: String,
    var ratingsContent: String,
    var ratingsSnooze: String,
    var ratingsHeadlineResult: String,
    var ratingsContentResult: String,
    var ratingsSnoozeButton: String,
    var ratingsDialog: Boolean,
    var ratingsSnoozeButtonResult: String
) {
    fun copyWith(
        buttonWithTrial: String = this.buttonWithTrial,
        buttonWithoutTrial: String = this.buttonWithoutTrial,
        buttonWithIntroductoryPrice: String = this.buttonWithIntroductoryPrice,
        cancelButton: String = this.cancelButton,
        continueLimitedVersion: String = this.continueLimitedVersion,
        socialProofBottomLine: String = this.socialProofBottomLine,
        socialProofHeadline: String = this.socialProofHeadline,
        socialProofSubline: String = this.socialProofSubline,
        removeAdsHeadline: String = this.removeAdsHeadline,
        removeAdsContent: String = this.removeAdsContent,
        cancelTimer: Int = this.cancelTimer,
        confirmExit: String = this.confirmExit,
        tutorialButton: String = this.tutorialButton,
        tutorialUrl: String = this.tutorialUrl,
        paywallBulletPoints: List<String> = this.paywallBulletPoints,
        removeAdsButton: String = this.removeAdsButton,
        recoveryNotificationInterval: Int = this.recoveryNotificationInterval,
        enforcedAppVersion: String = this.enforcedAppVersion,
        forceUpdateTitle: String = this.forceUpdateTitle,
        forceUpdateSubtitle: String = this.forceUpdateSubtitle,
        forceUpdateDescription: String = this.forceUpdateDescription,
        buttonText: String = this.buttonText,
        dismissText: String = this.dismissText,
        enforcedAppVersionLastAccepted: String = this.enforcedAppVersionLastAccepted,
        ratingsHeadline: String = this.ratingsHeadline,
        ratingsContent: String = this.ratingsContent,
        ratingsSnooze: String = this.ratingsSnooze,
        ratingsHeadlineResult: String = this.ratingsHeadlineResult,
        ratingsContentResult: String = this.ratingsContentResult,
        ratingsSnoozeButton: String = this.ratingsSnoozeButton,
        ratingsDialog: Boolean = this.ratingsDialog,
        ratingsSnoozeButtonResult: String = this.ratingsSnoozeButtonResult
    ): FirebaseConfigurations {
        return FirebaseConfigurations(
            buttonWithTrial, buttonWithoutTrial, buttonWithIntroductoryPrice, cancelButton,
            continueLimitedVersion, socialProofBottomLine, socialProofHeadline, socialProofSubline,
            removeAdsHeadline, removeAdsContent, cancelTimer, confirmExit, tutorialButton,
            tutorialUrl, paywallBulletPoints, removeAdsButton, recoveryNotificationInterval,
            enforcedAppVersion, forceUpdateTitle, forceUpdateSubtitle, forceUpdateDescription,
            buttonText, dismissText, enforcedAppVersionLastAccepted, ratingsHeadline,
            ratingsContent, ratingsSnooze, ratingsHeadlineResult, ratingsContentResult,
            ratingsSnoozeButton, ratingsDialog, ratingsSnoozeButtonResult
        )
    }

    companion object {
        fun fromJson(map: Map<String, Any?>): FirebaseConfigurations {
            return FirebaseConfigurations(
                buttonWithTrial = map["buttonWithTrial"] as? String ?: "",
                buttonWithoutTrial = map["buttonWithoutTrial"] as? String ?: "",
                buttonWithIntroductoryPrice = map["buttonWithIntroductoryPrice"] as? String ?: "",
                cancelButton = map["cancelButton"] as? String ?: "",
                continueLimitedVersion = map["continueLimitedVersion"] as? String ?: "",
                socialProofBottomLine = map["socialProofBottomLine"] as? String ?: "",
                socialProofHeadline = map["socialProofHeadline"] as? String ?: "",
                socialProofSubline = map["socialProofSubline"] as? String ?: "",
                removeAdsHeadline = map["removeAdsHeadline"] as? String ?: "",
                removeAdsContent = map["removeAdsContent"] as? String ?: "",
                cancelTimer = (map["cancelTimer"] as? Number)?.toInt() ?: 0,
                confirmExit = map["confirmExit"] as? String ?: "",
                tutorialButton = map["tutorialButton"] as? String ?: "",
                tutorialUrl = map["tutorialUrl"] as? String ?: "",
                paywallBulletPoints = map["paywallBulletPoints"] as? List<String> ?: emptyList(),
                removeAdsButton = map["removeAdsButton"] as? String ?: "",
                recoveryNotificationInterval = (map["recoveryNotificationInterval"] as? Number)?.toInt() ?: 0,
                enforcedAppVersion = map["enforcedAppVersion"] as? String ?: "",
                forceUpdateTitle = map["forceUpdateTitle"] as? String ?: "",
                forceUpdateSubtitle = map["forceUpdateSubtitle"] as? String ?: "",
                forceUpdateDescription = map["forceUpdateDescription"] as? String ?: "",
                buttonText = map["buttonText"] as? String ?: "",
                dismissText = map["dismissText"] as? String ?: "",
                enforcedAppVersionLastAccepted = map["enforcedAppVersionLastAccepted"] as? String ?: "",
                ratingsHeadline = map["ratingsHeadline"] as? String ?: "",
                ratingsContent = map["ratingsContent"] as? String ?: "",
                ratingsSnooze = map["ratingsSnooze"] as? String ?: "",
                ratingsHeadlineResult = map["ratingsHeadlineResult"] as? String ?: "",
                ratingsContentResult = map["ratingsContentResult"] as? String ?: "",
                ratingsSnoozeButton = map["ratingsSnoozeButton"] as? String ?: "",
                ratingsDialog = map["ratingsDialog"] as? Boolean ?: false,
                ratingsSnoozeButtonResult = map["ratingsSnoozeButtonResult"] as? String ?: ""
            )
        }
    }

    fun toJson(): Map<String, Any?> {
        return mapOf(
            "buttonWithTrial" to buttonWithTrial,
            "buttonWithoutTrial" to buttonWithoutTrial,
            "buttonWithIntroductoryPrice" to buttonWithIntroductoryPrice,
            "cancelButton" to cancelButton,
            "continueLimitedVersion" to continueLimitedVersion,
            "socialProofBottomLine" to socialProofBottomLine,
            "socialProofHeadline" to socialProofHeadline,
            "socialProofSubline" to socialProofSubline,
            "removeAdsHeadline" to removeAdsHeadline,
            "removeAdsContent" to removeAdsContent,
            "cancelTimer" to cancelTimer,
            "confirmExit" to confirmExit,
            "tutorialButton" to tutorialButton,
            "tutorialUrl" to tutorialUrl,
            "paywallBulletPoints" to paywallBulletPoints,
            "removeAdsButton" to removeAdsButton,
            "recoveryNotificationInterval" to recoveryNotificationInterval,
            "enforcedAppVersion" to enforcedAppVersion,
            "forceUpdateTitle" to forceUpdateTitle,
            "forceUpdateSubtitle" to forceUpdateSubtitle,
            "forceUpdateDescription" to forceUpdateDescription,
            "buttonText" to buttonText,
            "dismissText" to dismissText,
            "enforcedAppVersionLastAccepted" to enforcedAppVersionLastAccepted,
            "ratingsHeadline" to ratingsHeadline,
            "ratingsContent" to ratingsContent,
            "ratingsSnooze" to ratingsSnooze,
            "ratingsHeadlineResult" to ratingsHeadlineResult,
            "ratingsContentResult" to ratingsContentResult,
            "ratingsSnoozeButton" to ratingsSnoozeButton,
            "ratingsDialog" to ratingsDialog,
            "ratingsSnoozeButtonResult" to ratingsSnoozeButtonResult
        )
    }
}
